import java.util.Date;

public class Buy {
    private int id;

    private int clientId;

    private int gameId;

    private Date date;

    private int amount;

    public Buy(int id, int clientId, int gameId, Date date, int amount) {
        this.id = id;
        this.clientId = clientId;
        this.gameId = gameId;
        this.date = date;
        this.amount = amount;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getClientId() {
        return clientId;
    }

    public void setClientId(int clientId) {
        this.clientId = clientId;
    }

    public int getGameId() {
        return gameId;
    }

    public void setGameId(int gameId) {
        this.gameId = gameId;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }

    public int getAmount() {
        return amount;
    }

    public void setAmount(int amount) {
        this.amount = amount;
    }
}
